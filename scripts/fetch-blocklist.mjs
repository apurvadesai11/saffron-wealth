#!/usr/bin/env node
// Fetches the SecLists "10 million top-10000" password list and regenerates
// lib/auth/blocklist-data.ts. Review the URL before running. Re-run whenever
// you want to refresh the bundled list. ~80KB of source added on success.
//
// Usage:  node scripts/fetch-blocklist.mjs

import { writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const SOURCE_URL =
  "https://raw.githubusercontent.com/danielmiessler/SecLists/master/Passwords/Common-Credentials/10-million-password-list-top-10000.txt";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_PATH = resolve(__dirname, "../lib/auth/blocklist-data.ts");

const res = await fetch(SOURCE_URL);
if (!res.ok) {
  console.error(`Fetch failed: ${res.status} ${res.statusText}`);
  process.exit(1);
}
const text = await res.text();
const passwords = text
  .split("\n")
  .map((line) => line.trim())
  .filter((line) => line.length > 0);

const escaped = passwords.map(
  (p) => `"${p.replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"`,
);

const out = `// Generated by scripts/fetch-blocklist.mjs from SecLists top-10k.
// Do not edit by hand. Re-run the script to refresh.
export const COMMON_PASSWORDS_RAW = [
${escaped.map((s) => "  " + s).join(",\n")},
] as const;
`;

writeFileSync(OUT_PATH, out, "utf8");
console.log(`Wrote ${passwords.length} passwords to ${OUT_PATH}`);
